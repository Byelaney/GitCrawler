package usefuldata;

/**
 *
 *存放所有commit记录
 *name为提交者的用户名
 *date 为提交时间
 *
 *@author Bacchus_yl
 */
public class CommitDate 
{
	private String name;
	private String date;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	

}